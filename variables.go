package main

import "fmt"

func main() {
	var a string = "Riya"
	fmt.Println(a)

	var b, c int = 1, 2
	fmt.Println(b, c)

	var d bool = true
	fmt.Println(d)

	f := "Hi Hello"
	fmt.Println(f)
	var j, k, l int = 0, 1, 2
	fmt.Println(j)
	fmt.Println(k)
	fmt.Println(l)
	var pi float64
	pi = 3.14
	fmt.Println(pi)
}
